/* is pol irreducible and primitive? */
function is_prim_pol(pol,p)
  m := Degree(pol);
  Fpz<z> := PolynomialRing(GF(p));
  pol := Fpz!pol;
  if not IsIrreducible(pol) then return false; end if;
  xoo := p^m-1;
  pps := [a[1]:a in Factorization(p^m-1)];
  for j in [1..#pps] do
    Fq<a> := ext<GF(p)|pol>;
    if (a^(xoo div pps[j])) eq 1 then
      return false;
    end if;
  end for;
  return true;
end function;


